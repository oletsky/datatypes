package javatraining.oletsky.datatypes;

public class DemoLogicalOperators {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(!a);
        System.out.println(a^b);

    }
}
