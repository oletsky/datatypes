package javatraining.oletsky.datatypes;

public class TypeInterpreting {
    public static void main(String[] args) {
        //Int and char
        int a=65;
        System.out.println(a); //65
        System.out.println((char) a); //A has such code

        //Int and byte
        int b =253;
        System.out.println(b); //253
        System.out.println((byte) b); //-3 (additional code}
    }
}
