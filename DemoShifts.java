package javatraining.oletsky.datatypes;

/**
 * Illustrating shifts
 */
public class DemoShifts {
    public static void main(String[] args) {
        int a = 6;
        System.out.println(a>>1); //3
        System.out.println(a<<1); //12
        System.out.println(a<<5); //192
        System.out.println(a>>2); //1
    }

}
